            一般的にサンフランシスコやロンドンなどビジネス都市ではフェア（見本市）やコンベンション（会議）時期が最も高く、平日が土・日曜日よりも高い傾向にあります。ラスベガスやハワイなどのリゾート都市ではイベント開催期間に次いでホリデイシーズンや金・土曜日は高い傾向があります。<br>
