            <div class="text">【旅券】旅券（パスポート）は、日本政府が発行するあなたの国際的身分証明書です。出入国審査、査証申請、航空会社やホテルのチェックイン等、海外旅行には命の次に大切といわれる必携書類です。旅行の計画を立てたら先ず有効期限を確認しましょう。旅券の有効期限までの残り期間を「残存有効期間」といいます。渡航国、滞在期間、渡航目的によって残存有効期間の長さが異なりますが、3～6ヵ月間以上必要になる国が多くあります。例えば、ドバイの場合は入国時に残存有効期間が6か月必要なので2024年1月1日に旅行する方には2024年7月1日まで有効な旅券が必要です。残存有効期間が足りないと入国できません。旅券は都道府県のホームページで取得方法をご確認ください。申請から受領まで約1週間ほどかかります。<br>
