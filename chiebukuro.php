            <div class="head">ホテルグレードについて</div>
            <div class="text">【ホテルグレード】ホテルの格付けには世界共通の基準もなく、民間会社や観光局から自称まで独自の評価でグレードを決定しています。レストランの格付けで有名なミシュランもホテルの格付けを公表しています。知らない土地でホテルを選択するために旅行者には便利ですが、実際には評価と大違いのこともあるので旅行者泣かせです。弊社は現地会社の評価に従い、下記の5段階にカテゴリーを分けています。<br>
            【ラグジュアリー】格式があり、世界的なホテルチェーンに属して受賞歴を持つ最高級のホテル。お客様評価も9割以上が満足し、有名レストラン、バー、スパ、プール等超一流の施設を持ち、客室にも貴賓室をはじめとした富裕層向け客室が多数揃っています。優秀なコンシェルジュやバトラー、24時間ルームサービスなどの人的サービスも行き届いています。<br>
            【デラックス】スイートやクラブフロアの設備もあり、客室の広さが最低30㎡以上でレストラン、バー、バンケット、パブリックスペース等の施設もゴージャスな高級ホテル。お客様も8割以上が満足し、コンシェルジュ、ベル、ドアマンなどの人的サービスも一流。交通も至便であったり、代表的なリゾートとして認知されています。<br>
