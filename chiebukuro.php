            <div class="head">レストランについて</div>
            <div class="text">【レストラン】パエリア（スペイン）、ザッハトルテ（オーストリア）、小籠包（台湾）等海外旅行の楽しみの一つが本場で食す名物料理ではないでしょうか？旬ならではの味覚も見逃せません。多くの観光客にとっても同じ気持ちですから有名レストランの予約は早めに行いたいものです。<br>
            日時、人数、料理内容、座席希望（テラスや窓側など）などを添えてご希望のレストランを予約しましょう。総合旅行の場合はミールコントロールも重要になってきます。高級レストランやディナークルーズなどはドレスコード（服装規定）があります。予約したレストランによって日本からの荷物にも影響があります。<br>
            洋食では3コース（前菜、メイン、デザート）が一般的ですが、メインが肉と魚の2種類出る4コースなら更に豪華な食卓となります。パリではPlat du jour（プラ・デュ・ジュール）と言う日替わりメニューもあって、ワインが付くこともありますが、水は有料になります。また、雰囲気の良いテラス席は同じコーヒーなのに店内より高いことに驚かされます。飲み放題は稀にありますが、食べ放題はビュッフェ以外に海外ではあまり見かけません。生水、氷、生卵は細菌が入っている怖れもありますので、日本より注意した方が良いでしょう。<br>
